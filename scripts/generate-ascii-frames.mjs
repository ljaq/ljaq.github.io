/**
 * Bundles data/ascii_frames/frame_*.txt (already processed/cropped) into lib/asciiFramesHome.ts.
 * Edit the .txt files directly; run: pnpm generate:ascii-frames
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.join(__dirname, '..')
const FRAMES_DIR = path.join(ROOT, 'data', 'ascii_frames')
const OUT = path.join(ROOT, 'lib', 'asciiFramesHome.ts')

function main() {
  const files = fs
    .readdirSync(FRAMES_DIR)
    .filter(f => f.startsWith('frame_') && f.endsWith('.txt'))
    .sort()

  if (files.length === 0) {
    console.error(`No frame_*.txt in ${FRAMES_DIR}`)
    process.exit(1)
  }

  const frames = files.map(f => fs.readFileSync(path.join(FRAMES_DIR, f), 'utf8').replace(/\r\n/g, '\n'))

  const header = `/* Auto-generated by scripts/generate-ascii-frames.mjs from data/ascii_frames */
`

  fs.writeFileSync(OUT, `${header}export const ASCII_FRAMES: string[] = ${JSON.stringify(frames)}\n`)
  console.log(`Wrote ${frames.length} frames to lib/asciiFramesHome.ts (${fs.statSync(OUT).size} bytes)`)
}

main()
